using Content.Shared.Body.Part;
using Content.Shared.Surgery;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Surgery.UI;

[GenerateTypedNameReferences]
public sealed partial class SurgeryEdgeDetails : PanelContainer
{
    [Dependency] private readonly IEntityManager _entity = default!;

    private readonly SpriteSystem _sprite;

    public SurgeryEdgeDetails(SurgeryEdge edge, EntityUid? body, EntityUid? limb, BodyPart part) : base()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _sprite = _entity.System<SpriteSystem>();

        var icon = edge.Requirement.GetIcon(body, limb, part);
        RequirementIcon.Texture = icon == null ? null : _sprite.Frame0(icon);
        RequirementDescription.Text = edge.Requirement.Description(body, limb, part);
    }
}
